public class Librarian {
    private String Name;
    private int Age;
    private String Sex;
    private double Salary;
    private int Shift;
    private int Days_Off;

    public Librarian(String Name, int Age, String Sex, double Salary, int Shift, int Days_Off) {
        this.Name = Name;
        this.Age = Age;
        this.Sex = Sex;
        this.Salary = Salary;
        this.Shift = Shift;
        this.Days_Off = Days_Off;
    }
}
