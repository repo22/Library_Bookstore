public class Books {
    private int Year;
    private String Genre;
    private String Title;
    private String Author;
    private String Stock;

    public Books(int Year, String Genre, String Title, String Author, String Stock) {
        this.Year = Year;
        this.Genre = Genre;
        this.Title = Title;
        this.Author = Author;
        this.Stock = Stock;
    }
}
